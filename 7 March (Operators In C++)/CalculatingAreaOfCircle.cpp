                                        //Q2. Calculating Area of a Circle

#include<iostream>
using namespace std;
int main(){
    float r,Area;
    float pie=3.14;
    cout<<"Enter the radius: = ";
    cin>>r;

    Area=pie*r*r;
    cout<<"Area of Circle: = "<<Area;
    return 0;
}

